package hdf.pattern.homeTheater.facade;

public class PopcornPopper {
    public void on() {
        System.out.println("Popcorn popper is on");
    }

    public void pop() {
        System.out.println("Popcorn start cooking");
    }

    public void off() {
        System.out.println("Popcorn popper is off");
    }
}
